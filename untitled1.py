"""### 6. Naive Bayes (Adapted for Regression)"""

from sklearn.naive_bayes import GaussianNB
from sklearn.preprocessing import KBinsDiscretizer
import numpy as np

# Discretize the target variable (Salary) into bins
# For simplicity, let's use 5 bins. The strategy can be adjusted.
discretizer = KBinsDiscretizer(n_bins=5, encode='ordinal', strategy='uniform', subsample=None, random_state=42)
y_train_binned = discretizer.fit_transform(y_train.values.reshape(-1, 1))
y_test_binned = discretizer.transform(y_test.values.reshape(-1, 1))

# Create a mapping from bin index to mean salary value from the training data
bin_means = []
for i in range(discretizer.n_bins_[0]):
    # Get the indices of y_train that fall into this bin
    bin_indices = np.where(y_train_binned.flatten() == i)[0]
    if len(bin_indices) > 0:
        bin_means.append(y_train.iloc[bin_indices].mean())
    else:
        # If a bin is empty, assign the mean of the entire y_train as a fallback
        bin_means.append(y_train.mean())

# Initialize and train the Gaussian Naive Bayes model
nb_model = GaussianNB()
nb_model.fit(X_train, y_train_binned.flatten())

# Make predictions (predict bins first)
y_pred_binned_nb = nb_model.predict(X_test)

# Convert predicted bins back to salary values using the bin_means mapping
y_pred_nb = np.array([bin_means[int(bin_idx)] for bin_idx in y_pred_binned_nb])

# Evaluate the model
mae_nb = mean_absolute_error(y_test, y_pred_nb)
mse_nb = mean_squared_error(y_test, y_pred_nb)
r2_nb = r2_score(y_test, y_pred_nb)

print(f"Naive Bayes (Adapted) MAE: {mae_nb:.2f}")
print(f"Naive Bayes (Adapted) MSE: {mse_nb:.2f}")
print(f"Naive Bayes (Adapted) R2 Score: {r2_nb:.2f}")



